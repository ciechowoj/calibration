    points = hstack((points, sample_cube(num, center = size * [-0.5, 0.0, 0], size = size * [0, 1, 1])))
    points = hstack((points, sample_cube(num, center = size * [0.5, 0.0, 0], size = size * [0, 1, 1])))
    points = hstack((points, sample_cube(num, center = size * [0, 0.0, -0.5], size = size * [1, 1, 0])))
    points = hstack((points, sample_cube(num, center = size * [0, 0.0, 0.5], size = size * [1, 1, 0])))

    return (points.T + append(center, 0)).T

