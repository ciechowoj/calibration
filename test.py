def make_remove_locations(W, percent):
    assert(0.0 <= percent and percent <= 1.0)
    import random

    result = set()

    m, n = W.shape
    m = m // 3
    remove = int(m * n * percent)
    removed = 0

    while removed < remove:
        i = random.randint(0, m - 1)
        j = random.randint(0, n - 1)

        while (i, j) in result:
            if j == n - 1:
                j = 0
                i = (i + 1) % m
            else:
                j += 1

        result.add((i, j))

        removed += 1

    return list(result)


def remove_point_locations(W, locations):
    if not isinstance(locations, list):
        return remove_point_locations(W, make_remove_locations(W, locations))
    else:
        m, n = W.shape
        result = W.copy()

        for i, j in locations:
            if i < m and j < n:
                result[i * 3 + 0, j] = NaN
                result[i * 3 + 1, j] = NaN
                result[i * 3 + 2, j] = NaN

        return result

